import { CATEGORY_EXECUTION_SET } from './constants.js'

function play(def) {
  return {
    positioning: def.positioning,
    priority: def.priority,
    reading: def.reading,
    firstOption: def.first,
    secondOption: def.second,
    thirdOption: def.third,
    executionSet: CATEGORY_EXECUTION_SET[def.category] ?? 'isolation',
    tags: def.tags ?? [],
    needsBig: Boolean(def.needsBig),
    needsShooter: Boolean(def.needsShooter),
    needsCreator: Boolean(def.needsCreator),
    transitionOnly: Boolean(def.transitionOnly),
    ...def,
  }
}

